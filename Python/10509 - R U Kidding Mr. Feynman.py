while(1):
    n=float(input())
    # print(n)
    if(n==0.0):
        # print(int(n))
        break
    cubic_root=0
    i=1
    while(1):  # infinite loop
        if(i**3>n):
            cubic_root=i-1
            break
        else:
            i=i+1
    
    dx=(n-cubic_root**3)/(3*cubic_root**2)
    ans=cubic_root+dx 
    print('%.4f'%ans)